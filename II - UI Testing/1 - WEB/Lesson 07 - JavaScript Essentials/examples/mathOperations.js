var a = 10, b = 3, c;
// Сложение
c = a + b; //10+3=13
// Вычетание
c = a - b; //10-3=7
// Умножение
c = a * b; //10*3=30
// Деление
c = a / b; //10/3=3.3333
// Возведение в степень
c = a ** b; //10**3=1000
// Деление по модулю 
//(оствток от деления)
c = a % b;//10%3=1
//Инкремент
c++; //с=с+1 
//Декремент
c--; //c=c-1
//Префиксный инкремент
let d = ++a; //a=a+1 d=a
//Постфиксный инкремент
let e = b++; //e=b b=b+1
// a=11, b=4, d=11, e=3
console.log(`a=${a}, b=${b}, d=${d}, e=${e}`);