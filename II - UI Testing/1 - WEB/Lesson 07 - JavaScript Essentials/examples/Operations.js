var a = 10, b = 3, c;
// Сложение
c = a + b;
// Вычетание
c = a - b;
// Умножение
c = a * b;
// Деление
c = a / b;
// Возведение в степень
c = a ** b;
// Деление по модулю 
//(оствток от деления)
c = a % b;
//Инкремент
c++; //с=с+1
//Декремент
c--; //c=c-1
//Префиксный инкремент
let d = ++a; //a=a+1 d=a
//Постфиксный инкремент
let e = b++; //e=b b=b+1
// a=11, b=4, d=11, e=3
console.log(`a=${a}, b=${b}, d=${d}, e=${e}`);