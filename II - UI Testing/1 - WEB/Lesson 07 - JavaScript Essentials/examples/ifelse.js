var a = 10;
//Конструкция 1: a==10
if (a == 10) {
    console.log("a==10");
}

//Конструкция 2: a==0 else...
if (a == 0) {
    console.log("a==0");
}
else {
    console.log("a==0 else...");
}

//Конструкция 3: a!=1 && a!=2
if (a == 1) {
    console.log("a==1");
}
else if (a == 2) {
    console.log("a==2");
}
else {
    console.log("a!=1 && a!=2");
}